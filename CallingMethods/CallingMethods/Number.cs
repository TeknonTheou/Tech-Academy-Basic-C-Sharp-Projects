using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CallingMethods
{
    public class Number
    {
        public int addFive(int num)//define addFive method
        {
            return num + 5;//add 5
        }
        public int timesFive(int num)//define timesFive method
        {
            return num * 5;//multiply by 5
        }
        public int divTwo(int num)//define divTwo method
        {
            return num / 2;//divide by 2
        }
    }
}
